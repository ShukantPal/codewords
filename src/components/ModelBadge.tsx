import type { TeamModelConfig } from '@/interfaces/models';

type ModelBrand = {
  key: string;
  label: string;
};

function modelBrand(model: Pick<TeamModelConfig, 'provider' | 'name'>): ModelBrand {
  const id = `${model.provider}/${model.name}`.toLowerCase();
  if (id.includes('gpt') || id.includes('openai')) {
    return { key: 'openai', label: 'OpenAI' };
  }
  if (id.includes('deepseek')) {
    return { key: 'deepseek', label: 'DeepSeek' };
  }
  if (id.includes('llama') || id.includes('meta')) {
    return { key: 'meta', label: 'Meta Llama' };
  }
  if (id.includes('qwen')) {
    return { key: 'qwen', label: 'Qwen' };
  }
  if (id.includes('minimax')) {
    return { key: 'minimax', label: 'MiniMax' };
  }
  if (id.includes('novita')) {
    return { key: 'novita', label: 'Novita' };
  }
  return { key: 'generic', label: model.provider };
}

